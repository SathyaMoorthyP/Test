package week1.day2;

public class LearnForLoop {
	
	public static void main(String[] args) {
		// Need to print 1 to 100
		
		//initialization; Condition; increment/decrement
		// i++ = i=i+1
		
		for (int i = 1; i <=5 ; i++) {
			System.out.println(i);
		}
		for (int i = 5; i >= 1 ; i--) {
			System.out.println(i);
		}
	}
		
}

